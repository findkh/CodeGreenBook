package Review211025;

public class Ex05_TypeConversion2 {

	public static void main(String[] args) {
		System.out.println("1"+"2");
//		문자열+문자열 12가 출력
		System.out.println(true+"");
//		문자열로 변환됨
		System.out.println('A'+'B');
//		문자+문자 아스키코드 65+66으로 131이 출력됨
		System.out.println('1'+2);
//		1의 아스키코드 49에 더하기 2를 하면 51
//		문자로 나타내고 싶으면 char로 형변환해야함
		System.out.println((char)('1'+2));
//		문자 3이 나옴
		System.out.println('J'+"AVA");
//		문자더하기 문자열은 문자열 JAVA
	

	}

}
